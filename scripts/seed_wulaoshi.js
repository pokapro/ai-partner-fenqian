function getToken() {
  // 1. 直接从环境读取
  if (process.env[REQUIRED_ENV]) {
    return process.env[REQUIRED_ENV];
  }
  // 2. 尝试从 ~/.env 读取
  try {
    const fs = require('fs');
    const path = require('path');
    const envPath = path.join(require('os').homedir(), '.env');
    if (fs.existsSync(envPath)) {
      const lines = fs.readFileSync(envPath, 'utf-8').split('\n');
      for (const line of lines) {
        const match = line.match(/^ADMIN_TOKEN\s*=\s*(.+)$/);
        if (match) return match[1].trim().replace(/['"]/g, '');
      }
    }
  } catch (_) { /* ignore */ }
  return null;
}

async function apiPost(endpoint, body) {
  const url = `${API_BASE}${endpoint}?token=${encodeURIComponent(token)}`;
  const res = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  });
  const data = await res.json();
  if (!res.ok) {
    throw new Error(`POST ${endpoint} → ${res.status}: ${JSON.stringify(data)}`);
  }
  return data;
}

async function apiGet(endpoint) {
  const url = `${API_BASE}${endpoint}?token=${encodeURIComponent(token)}`;
  const res = await fetch(url);
  const data = await res.json();
  if (!res.ok) {
    throw new Error(`GET ${endpoint} → ${res.status}: ${JSON.stringify(data)}`);
  }
  return data;
}

