// 1. 获取 token
const token = getToken();
if (!token) {
  console.error('❌ 未找到 ADMIN_TOKEN');
  console.error(`   请设置环境变量: export ADMIN_TOKEN=xxx`);
  console.error(`   或写入 ~/.env 文件: ADMIN_TOKEN=xxx`);
  process.exit(1);
}
console.log(`🔑 Admin Token: ${token.substring(0, 4)}****${token.substring(token.length - 4)}`);
console.log(`🌐 API: ${API_BASE}\n`);

let inserted = { cases: 0, rules: 0, templates: 0 };

async function main() {
  // ──────── A. 注入知识案例 ────────
  console.log('━'.repeat(50));
  console.log('📚 注入知识案例...');
  for (const kc of KNOWLEDGE_CASES) {
    try {
      const res = await apiPost('/api/admin/knowledge-cases', kc);
      console.log(`  ✅ [${kc.id}] ${kc.title.substring(0, 40)}...`);
      inserted.cases++;
    } catch (err) {
      console.error(`  ❌ [${kc.id}] ${err.message}`);
    }
  }
  console.log(`   ➜ 知识案例: ${inserted.cases} 条\n`);

  // ──────── B. 注入规则 ────────
  console.log('━'.repeat(50));
  console.log('📏 注入规则...');
  for (const rule of RULES) {
    try {
      const res = await apiPost('/api/admin/rules', rule);
      console.log(`  ✅ [${rule.id}] ${rule.rule_name}`);
      inserted.rules++;
    } catch (err) {
      console.error(`  ❌ [${rule.id}] ${err.message}`);
    }
  }
  console.log(`   ➜ 规则: ${inserted.rules} 条\n`);

  // ──────── C. 注入条款模板 ────────
  console.log('━'.repeat(50));
  console.log('📝 注入条款模板...');
  for (const tpl of TEMPLATES) {
    try {
      const res = await apiPost('/api/admin/templates', tpl);
      console.log(`  ✅ [${tpl.id}] ${tpl.title.substring(0, 40)}...`);
      inserted.templates++;
    } catch (err) {
      console.error(`  ❌ [${tpl.id}] ${err.message}`);
    }
  }
  console.log(`   ➜ 条款模板: ${inserted.templates} 条\n`);

  // ──────── D. 验证 —— 查询总数 ────────
  console.log('━'.repeat(50));
  console.log('🔍 验证数据注入结果...');
  try {
    const [cases, rules, templates] = await Promise.all([
      apiGet('/api/admin/knowledge-cases'),
      apiGet('/api/admin/rules'),
      apiGet('/api/admin/templates'),
    ]);
    console.log(`   ✅ 知识案例: ${cases.length} 条`);
    console.log(`   ✅ 规则: ${rules.length} 条`);
    console.log(`   ✅ 条款模板: ${templates.length} 条`);
  } catch (err) {
    console.error(`   ❌ 验证失败: ${err.message}`);
  }

  console.log('\n' + '━'.repeat(50));
  console.log(`🎉 种子数据注入完成！`);
  console.log(`   本次新增: ${inserted.cases} 案例 + ${inserted.rules} 规则 + ${inserted.templates} 模板`);
  console.log(`   来源: 吴老师U盘股权资料\n`);
}

main().catch(err => {
  console.error('\n❌ 脚本运行失败:', err);
  process.exit(1);
});
