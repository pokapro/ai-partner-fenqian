// 输出形态：三份完整协议草案 + 用户原话响应表 + 法律边界声明
//   协议一：《股东合作协议书》
//   协议二：《股权代持协议》
//   协议三：《一致行动人协议》
//
// 调用方式：
//   const { buildProtocolPackPrompt, buildProtocolPackUserPrompt } = require('./prompt_dt_protocol');
